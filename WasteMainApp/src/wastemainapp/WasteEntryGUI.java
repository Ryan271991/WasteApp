    private boolean isEircode(String eircode) {
        ArrayList<HouseHold> hList; // declare variable hList (no data)
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("household.dat"))) { //open the household.dat file
            //Read household.dat file -> list Household
            hList = (ArrayList<HouseHold>) ois.readObject(); // read the data and cast to Arraylist<HouseHold>
            //loop all Household in the file
            for (int i = 0; i < hList.size(); i++) {
                HouseHold h = hList.get(i);
                //check eircode
                if (h.getEircode().equalsIgnoreCase(eircode)) {
                    return true; // found
                }
            }
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "The data file household.dat was not found. Please save before trying to load.");
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while reading the file. Make sure the class matches the saved data");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while reading the file." + e.getMessage());
        }
        return false; // not found
    }
