    private void saveBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtn1ActionPerformed
        // TODO add your handling code here:
        // get the data
        eircode = eircodeTF.getText().trim();
        String weightStr = weightTF.getText().trim(); // because weight is double current so it need to change String to get data
        date = dateTF.getText().trim();
        // check empty data
        if (eircode.isEmpty() || weightStr.isEmpty() || date.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fill all information please!");
            return;
        }
        //parse weight
        try {
            weight = Double.parseDouble(weightStr);
            if (weight <= 0) {
                JOptionPane.showMessageDialog(this, "The weight must be greater than 0");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "The weight must be a number");
            return;
        }
        //check Eircode
        if (!isEircode(eircode)) {
            JOptionPane.showMessageDialog(this, "The Eircode is not registered");
            return;
        }
        // Waste sorting and save by type button
        if (recyclingRB.isSelected()) {
            wasteType = "Recycling";
            file = "recycling.dat";
        } else if (organicwasteRB.isSelected()) {
            wasteType = "Organic Waste";
            file = "organic.dat";
        } else if (generalwasteRB.isSelected()) {
            wasteType = "General Waste";
            file = "general.dat";
        } else {
            JOptionPane.showMessageDialog(this, "Please select a waste type.");
            return;
        }
        // load the data
        list = new ArrayList<>();
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream(file))) {
            list = (ArrayList<WasteEntry>) ois.readObject();
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "The data file was not found. Please save before trying to load.");
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while reading the file. Make sure the class matches the saved data");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while reading the file." + e.getMessage());
        }
        // create a new object (wasteEntry)
        WasteEntry w = new WasteEntry(eircode, weight, wasteType, date);
        list.add(w); //Add in current list
        //save 
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(file))) {
            oos.writeObject(list);
            JOptionPane.showMessageDialog(this, "Saved entry to " + file + " successfully!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving file: " + e.getMessage());
        }
    }//GEN-LAST:event_saveBtn1ActionPerformed
   
    private void load() {
        //identify file by radio 
        if (recyclingRB.isSelected()) {
            file = "recycling.dat";
        } else if (organicwasteRB.isSelected()) {
            file = "organic.dat";
        } else if (generalwasteRB.isSelected()) {
            file = "general.dat";
        } else {
            JOptionPane.showMessageDialog(this, "Please select a waste type.");
            list = new ArrayList<>(); //avoid null
            return;
        }
        try (ObjectInputStream oStream = new ObjectInputStream(new FileInputStream(file))) {

            list = (ArrayList<WasteEntry>) oStream.readObject();
            JOptionPane.showMessageDialog(this, "File loaded successfully");

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this,
                    "The data file " + file + " was not found. Please save before trying to load.");
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this,
                    "An error occurred while reading the file. Make sure the class matches the saved data");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this,
                    "An error occurred while reading the file. " + e.getMessage());
            list = new ArrayList<>();
        }

    }
    
