    private void load(){
      //  houseHold = new ArrayList<>();//always a empty list already
        try(ObjectInputStream oStream = new ObjectInputStream(new FileInputStream("household.dat"))){ //open file to read
            houseHold = (ArrayList<HouseHold>)oStream.readObject(); // read data and cast to ArrayList<HouseHold> and put result in the household
            JOptionPane.showMessageDialog(this,"File loaded successfully"); // show message
        }
        catch(FileNotFoundException e){ //file not found
            houseHold = new ArrayList<>(); //if don't have file ==> create list empty to avoid null
        }
        catch(ClassNotFoundException e){ // class not found (changed name)
            JOptionPane.showMessageDialog(this,"An error occurred while reading the file. Make sure the class matches the saved data");
        }
        catch(IOException e){ //file wrong, wrong format..
            JOptionPane.showMessageDialog(this,"An error occurred while reading the file."+e.getMessage());
            houseHold = new ArrayList<>(); 
        }
    }
    
