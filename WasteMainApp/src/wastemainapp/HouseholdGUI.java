    private boolean isEircode(String eircode) {
    for (int i = 0; i < houseHold.size(); i++) { //loop from 0 to the end
        HouseHold h = houseHold.get(i); // Each iteration will take one HouseHold to check. 
        if (h.getEircode().equalsIgnoreCase(eircode)) { //If the eircode is the same (case insensitive)
            return true;  // eircode is exists
        }
    }
    return false; // not exists
}

